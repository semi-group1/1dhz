package semi.model;

public class EditInfoForm {
	private String email;
	private String passwd;
	private String passwdCheck;
	private String name;
	private String sup_job;
	private String sub_job;
	private String comment;
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public String getPasswdCheck() {
		return passwdCheck;
	}
	public void setPasswdCheck(String passwdCheck) {
		this.passwdCheck = passwdCheck;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSup_job() {
		return sup_job;
	}
	public void setSup_job(String sup_job) {
		this.sup_job = sup_job;
	}
	public String getSub_job() {
		return sub_job;
	}
	public void setSub_job(String sub_job) {
		this.sub_job = sub_job;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	
	
}
